private void botonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCalcularActionPerformed
    Metodos.validarTextArea(textVector1);
    Metodos.validarTextArea(textVector2);
    String texto1=textVector1.getText();
    String texto2=textVector2.getText();
    if(Metodos.validarVector(texto1)&&textVector1.getLineCount()==1
            &&Metodos.validarVector(texto2)&&textVector2.getLineCount()==1){
        try{
            vector1=Metodos.leerVector(texto1);
            vector2=Metodos.leerVector(texto2);
            if(vector1.length==2&&vector2.length==2){
                angulo=Metodos.ánguloEntreVectores(vector1,vector2);
                base11=Metodos.baseOrtogonal1(vector1,vector2);
                base12=Metodos.baseOrtogonal2(vector1,vector2);
                base21=Metodos.baseOrtogonal1(vector2,vector1);
                base22=Metodos.baseOrtogonal2(vector2,vector1);
                Metodos.imprimirResultado(angulo,textAngulo);
                Metodos.imprimirVector(base11,textResultado11);
                Metodos.imprimirVector(base12,textResultado12);
                Metodos.imprimirVector(base21,textResultado21);
                Metodos.imprimirVector(base22,textResultado22);
            }else{
                Metodos.error("El tamaño de los vectores no es igual");
            }
        }catch(Exception ex){
            Metodos.error(ex);
        }
    }else Metodos.error("Los datos ingresados no corresponden a vectores");
}//GEN-LAST:event_botonCalcularActionPerformed

