    private void botQuitarPrivActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botQuitarPrivActionPerformed
        int opcion = JOptionPane.showConfirmDialog(null, "Esta seguro de revocar este privilegio", "REVOCAR PRIVILEGIO", 2);
        if (opcion == 0) {
            Consulta.realizarConsulta("revoke " + listaPrivilegios.getSelectedValue() + " from " + listaRoles.getSelectedValue());
            Consulta.realizarConsultaLista("select privilege from dba_sys_privs where grantee='" + listaRoles.getSelectedValue() + "' order by privilege", listaPrivilegios);
            Consulta.realizarConsultaLista("select distinct privilege from dba_sys_privs order by privilege", listaPrivAgregar);
        }

    }//GEN-LAST:event_botQuitarPrivActionPerformed

