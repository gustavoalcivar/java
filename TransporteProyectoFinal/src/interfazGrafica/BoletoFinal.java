    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        cliente.setText("");
        rutaViaje.setText("");
        unidad.setText("");
        horaViaje.setText("");
        fechaViaje.setText("");
        asientosEspeciales.setText("");
        asientosNormales.setText("");
        costoTotal.setText("");
        buscado = null;
        if (!codigoBoletoABuscar.getText().equals("")) {
            Boleto boleto = new Boleto();
            boleto.setCodigoBoleto(codigoBoletoABuscar.getText());
            try {
                buscado = listaBoletos.obten(boleto);
            } catch (FachadaException fe) {
                JOptionPane.showMessageDialog(null, "Error: " + fe.getMessage());
            }
            if (buscado != null) {
                cliente.setText(buscado.getApellidoCliente() + " "
                        + buscado.getNombreCliente() + "            " + buscado.getCedulaCliente());
                rutaViaje.setText(buscado.getRuta());
                unidad.setText(buscado.getUnidad());
                horaViaje.setText(String.valueOf(buscado.getHora()));
                fechaViaje.setText(buscado.getFecha());
                asientosEspeciales.setText(String.valueOf(buscado.getNumAsientosEspeciales()) + " : "
                        + String.valueOf(buscado.getDetalleAsientosEspeciales()));
                asientosNormales.setText(String.valueOf(buscado.getNumAsientosNormales()) + " : "
                        + String.valueOf(buscado.getDetalleAsientosNormales()));
                costoTotal.setText(String.valueOf(buscado.getCostoTotal()));
            } else {
                JOptionPane.showMessageDialog(null, "El boleto no existe");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese el código del boleto a buscar");
        }
    }//GEN-LAST:event_buscarActionPerformed
