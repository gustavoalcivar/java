    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        if(codigo.getText().equals("")||matricula.getText().equals("")||
                capacidad.getText().equals("")||chofer.getText().equals("")||
                ayudante.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Debe llenar todos los campos");
        }else{
            Unidad unidad=new Unidad();
            unidad.setCodigoUnidad(codigo.getText());
            unidad.setMatricula(matricula.getText());
            unidad.setCapacidadUnidad(Integer.parseInt(capacidad.getText()));
            unidad.setDisponibles(Integer.parseInt(capacidad.getText()));
            unidad.setAyudante(ayudante.getText());
            unidad.setChofer(chofer.getText());

            IFachadaUnidad unidades=new ListadoUnidades();
            try{
                unidades.agrega(unidad);
                JOptionPane.showMessageDialog(null,"Unidad agergada");
                AgregarUnidades me=new AgregarUnidades();
                dispose();
                me.setVisible(true);
            }
              catch(FachadaException fe){
                JOptionPane.showMessageDialog(null,"Error: " + fe.getMessage());
            }
             
        }
    }//GEN-LAST:event_guardarActionPerformed

